LRELU_SLOPE = 0.1


class ResBlock1(torch.nn.Module):
    def __init__(self, h, channels, kernel_size=3, dilation=(1, 3, 5)):
        super(ResBlock1, self).__init__()
        self.h = h
        self.convs1 = nn.ModuleList([
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=dilation[0],
                               padding=get_padding(kernel_size, dilation[0]))),
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=dilation[1],
                               padding=get_padding(kernel_size, dilation[1]))),
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=dilation[2],
                               padding=get_padding(kernel_size, dilation[2])))
        ])
        self.convs1.apply(init_weights)

        self.convs2 = nn.ModuleList([
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=1,
                               padding=get_padding(kernel_size, 1))),
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=1,
                               padding=get_padding(kernel_size, 1))),
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=1,
                               padding=get_padding(kernel_size, 1)))
        ])
        self.convs2.apply(init_weights)

    def forward(self, x):
        for c1, c2 in zip(self.convs1, self.convs2):
            xt = F.leaky_relu(x, LRELU_SLOPE)
            xt = c1(xt)
            xt = F.leaky_relu(xt, LRELU_SLOPE)
            xt = c2(xt)
            x = xt + x
        return x

    def remove_weight_norm(self):
        for l in self.convs1:
            remove_weight_norm(l)
        for l in self.convs2:
            remove_weight_norm(l)


class ResBlock2(torch.nn.Module):
    def __init__(self, h, channels, kernel_size=3, dilation=(1, 3)):
        super(ResBlock2, self).__init__()
        self.h = h
        self.convs = nn.ModuleList([
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=dilation[0],
                               padding=get_padding(kernel_size, dilation[0]))),
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=dilation[1],
                               padding=get_padding(kernel_size, dilation[1])))
        ])
        self.convs.apply(init_weights)

    def forward(self, x):
        for c in self.convs:
            xt = F.leaky_relu(x, LRELU_SLOPE)
            xt = c(xt)
            x = xt + x
        return x

    def remove_weight_norm(self):
        for l in self.convs:
            remove_weight_norm(l)

class EncResBlock(torch.nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.convs = nn.ModuleList([
            Conv2d(in_ch, out_ch, kernel_size=3, padding=1),
            BatchNorm2d(out_ch),
            nn.ReLU()
        ])
        self.out_ch = out_ch
        self.in_ch = in_ch

    def forward(self, x):
        for c in self.convs:
            res = c(x)
            if self.out_ch == self.in_ch:
                x = res + x
            else:
                x = res
        return x
    
class Encoder(torch.nn.Module):
    def __init__(self, h):
        super().__init__()
        
        self.encs = nn.ModuleList()
        middle = h.n_blocks//2 + 1
        for i in range(1, h.n_blocks+1):
            if i < middle:
                self.encs.append(EncResBlock(4,4))
            elif i == middle:
                self.encs.append(EncResBlock(4,1))
            else:
                self.encs.append(EncResBlock(1,1))
                
        self.linear = nn.Linear(h.win_size//2+1,h.latent_dim)
        self.dropout = nn.Dropout(h.latent_dropout)
    
    def forward(self, x):
        # x: (B, 4, N, T)
        for enc_block in self.encs:
            x = enc_block(x)
        
        x = x.squeeze(1).transpose(1,2) # (B, 1, N, T) -> (B, T, N)
        x = self.linear(x)
        #! Apply dropout (according to DAE) to increase decoder robustness,
        #! because representation predicted from AM is used in TTS application.
        x = self.dropout(x)
        
        return x

