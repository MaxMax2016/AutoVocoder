class Generator(torch.nn.Module):
    def __init__(self, h):
        super().__init__()
        
        self.linear = nn.Linear(h.latent_dim, h.win_size//2+1)
        self.decs = nn.ModuleList()
        middle = h.n_blocks//2 + 1
        for i in range(1, h.n_blocks+1):
            if i < middle:
                self.decs.append(EncResBlock(1,1))
            elif i == middle:
                self.decs.append(EncResBlock(1,4))
            else:
                self.decs.append(EncResBlock(4,4))
                
        self.conv_post = Conv2d(4,2,3,1,padding=1) #! Predict Real/Img
        self.reflection_pad = nn.ReflectionPad1d((1, 0))
        self.stft = TorchSTFT(filter_length=h.n_fft, hop_length=h.hop_size, win_length=h.win_size)
        
    
    def forward(self, x):
        # x: (B, T, D)
        x = self.linear(x)
        x = x.transpose(1,2).unsqueeze(1)
        for dec_block in self.decs:
            x = dec_block(x)
        
        # (B, 4, N, T)
        x = F.leaky_relu(x)
        x = x.contiguous().view(x.size(0),-1,x.size(-1)) # (B, 4N, T)
        x = self.reflection_pad(x)
        x = x.contiguous().view(x.size(0),4,-1,x.size(-1)) # (B, 4N, T') -> (B, 4, N, T')
        # (B, 4, N, T') -> (B, 2, N, T')
        x = self.conv_post(x)
        real = x[:,0,:,:]
        imag = x[:,1,:,:]
        # real, imag = x.chunk(2, dim=-1)
        # real = real.squeeze(1)
        # imag = imag.squeeze(1)
        wav = self.stft.cartesian_inverse(real, imag)
        
        return wav
 
